def page_lastmod_iso(page: dict) -> str:
    updated = str(page.get("updated") or "").strip()
    for pattern in ("%d.%m.%Y", "%Y-%m-%d"):
        if updated:
            try:
                return datetime.strptime(updated, pattern).date().isoformat()
            except ValueError:
                continue
    return datetime.now(timezone.utc).date().isoformat()


